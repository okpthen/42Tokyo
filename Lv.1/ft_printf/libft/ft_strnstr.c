#include <stddef.h>

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;
	char	*str;

	str = (char *)big;
	if (!big)
		return (NULL);
	if (*little == '\0')
		return (str);
	i = 0;
	while (big[i] && i < len)
	{
		j = 0;
		while (little[j] && big[i + j] == little[j])
			j ++;
		if (little[j] == '\0' && i + j <= len)
			return (str + i);
		i ++;
	}
	return (NULL);
}
