#include "rush.h"

void	ft_gethint(int *hint, char *str)
{
	int	i;

	i = 0;
	while (*str)
	{
		if (*str >= '1' && *str <= '4')
		{
			hint[i] = *str - '0';
			i ++;
		}
		str ++;
	}
}
