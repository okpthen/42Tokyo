int	ft_iterative_factorial(int nb)
{
	int	result;

	result = 1;
	if (0 > nb)
		return (0);
	if (nb == 0)
		return (1);
	while (nb > 0)
	{
		result = result * nb;
		nb--;
	}
	return (result);
}

